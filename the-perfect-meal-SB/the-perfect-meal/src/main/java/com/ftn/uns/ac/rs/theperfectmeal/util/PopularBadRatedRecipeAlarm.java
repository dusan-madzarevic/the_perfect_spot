public class PopularBadRatedRecipeAlarm {

	private Long recipeId;

	public PopularBadRatedRecipeAlarm() {
		super();
	}

	public PopularBadRatedRecipeAlarm(Long recipeId) {
		super();
		this.recipeId = recipeId;
	}

	public Long getRecipeId() {
		return recipeId;
	}

	public void setRecipeId(Long recipeId) {
		this.recipeId = recipeId;
	}
	
}
