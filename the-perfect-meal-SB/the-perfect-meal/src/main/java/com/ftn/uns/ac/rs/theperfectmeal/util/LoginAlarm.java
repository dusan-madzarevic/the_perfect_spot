package com.ftn.uns.ac.rs.theperfectmeal.util;

public class LoginAlarm {

	private long userId;

	public LoginAlarm() {
		super();
	}

	public LoginAlarm(long userId) {
		super();
		this.userId = userId;
	}

	public long getUserId() {
		return userId;
	}

	public void setUserId(long userId) {
		this.userId = userId;
	}

}
